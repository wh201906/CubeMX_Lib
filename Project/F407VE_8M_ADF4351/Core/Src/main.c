/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  uint8_t str[30];
  uint32_t reg;
  double freq;
  ADF4351_CLKConfig CLK;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  /* USER CODE BEGIN 2 */
  Delay_Init(168);
  ADF4351_Init();
  ADF4351_SetCLKConfig(&CLK, 40, 10, 0, 1, 32, 0.001); // for 40MHz clock
  // ADF4351_SetCLKConfig(&CLK, 100, 25, 0, 1, 32, 0.001); // for 100MHz clock
  ADF4351_WriteCLKConfig(&CLK);
  MyUART_Init(&uart, USART1, uartBuf, 100);
  ADF4351_SetFreq(&CLK, 80.001);
  printf("ADF4351 Test\r\n");
  
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    Delay_ms(100);
    if(MyUART_ReadUntilWithEnd(&uart, str, '>'))
    {
      if(str[0] == 'f') // frequency
      {
        freq = myatof(str+1);
        printf("Freq set to %f\r\n", ADF4351_SetFreq(&CLK, freq));
      }
      else if(str[0] == 'c') // configure
      {
        ADF4351_WriteCLKConfig(&CLK);
        printf("WriteCLKConfig\r\n");
      }
      else if(str[0] == 'i') // init
      {
        ADF4351_Init();
        printf("Init\r\n");
      }
      else if(str[0] == 't') // test
      {
        if(str[1] == '1')
          ADF4351_Write(0x0000E1A9);
        else if(str[1] == '2')
          ADF4351_Write(0x01008E42);
        else if(str[1] == '3')
          ADF4351_Write(0x000404B3);
        else if(str[1] == '4')
          ADF4351_Write(0x00580005);
        else if(str[1] == '5')
          ADF4351_Write(0x0000E1A9);
        printf("Write to R%c\r\n", str[1]);
      }
      else if(str[0] == 'w') // write
      {
        reg = myatoi_hex(str+1);
        ADF4351_Write(reg);
        printf("Write 0x%x\r\n", reg);
      }
    }
  }
  /* USER CODE END 3 */
}

/**
  * @brief System Clock Configuration
  * @retval None
  */
void SystemClock_Config(void)
{
  RCC_OscInitTypeDef RCC_OscInitStruct = {0};
  RCC_ClkInitTypeDef RCC_ClkInitStruct = {0};

  /** Configure the main internal regulator output voltage
  */
  __HAL_RCC_PWR_CLK_ENABLE();
  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE1);
  /** Initializes the RCC Oscillators according to the specified parameters
  * in the RCC_OscInitTypeDef structure.
  */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSE;
  RCC_OscInitStruct.HSEState = RCC_HSE_ON;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE;
  RCC_OscInitStruct.PLL.PLLM = 4;
  RCC_OscInitStruct.PLL.PLLN = 168;
  RCC_OscInitStruct.PLL.PLLP = RCC_PLLP_DIV2;
  RCC_OscInitStruct.PLL.PLLQ = 4;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    Error_Handler();
  }
  /** Initializes the CPU, AHB and APB buses clocks
  */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV4;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV2;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_5) != HAL_OK)
  {
    Error_Handler();
  }
}

